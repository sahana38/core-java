class Furniture{

   static String name;
   static double price;
   static String color;


static void setName(String nm){
       name=nm;
}

static String getName(){
    return name;
}


static void setPrice(double rs){
       price= rs;
}

static double getPrice(){
    return price;
}


static void setColor(String clr){
    color = clr;
}

static String getColor(){
    return color;
}
}
