class Mannu{

  static String typeOfMannu;
  static double price;
  static String quantity;


  static void setTypeOfMannu(String mannu){
         typeOfMannu= mannu;
  }

  static String getTypeOfMannu(){
         return typeOfMannu;
   }

  static void setPrice(double rs){
      price = rs;
   }

  static double getPrice(){
       return price;
  }

  static void setQuantity(String q){
      quantity = q;
   }

  static String getQuantity(){
       return quantity;
  }
}